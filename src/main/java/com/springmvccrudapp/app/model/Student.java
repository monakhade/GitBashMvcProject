package com.springmvccrudapp.app.model;

public class Student 
{
	private int studentid;
	private String studentname;
	private String equcationdetails;
	private int age;
	private String address;
	public int getStudentid() {
		return studentid;
	}
	public void setStudentid(int studentid) {
		this.studentid = studentid;
	}
	public String getStudentname() {
		return studentname;
	}
	public void setStudentname(String studentname) {
		this.studentname = studentname;
	}
	public String getEqucationdetails() {
		return equcationdetails;
	}
	public void setEqucationdetails(String equcationdetails) {
		this.equcationdetails = equcationdetails;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	
	
}
